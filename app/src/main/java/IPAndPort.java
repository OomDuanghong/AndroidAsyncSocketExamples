public class IPAndPort {
    String ip;
    String port;

    public String getIP() {
        return ip;
    }

    public IPAndPort setIP(String ip) {
        this.ip = ip;
        return this;
    }
    public String getPort() {
        return port;
    }

    public IPAndPort setPort(String port) {
        this.port = port;
        return this;
    }
}
